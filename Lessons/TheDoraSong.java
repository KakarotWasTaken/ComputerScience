public class TheDoraSong
{
  public static void main (String[] args)
  {
    new TheDoraSong();
  }
  public TheDoraSong()
  {
    // Starting and Space
    System.out.println("\t Dora! Boots! Come on Dora!");
    System.out.println("\n");    
    
    // Part of lyrics
    System.out.println("D-d-d-d-d-dora");  
    System.out.print("\t D-d-d-d-d-dora \n");
    System.out.println("D-d-d-d-d-dora");
    System.out.println("\t D-d-d-d-d-dora \n");
    
    // Ending sentence
    System.out.println("Dora Dora Dora the \"explorer\". Who's that super cool exploradora?");
  }
}